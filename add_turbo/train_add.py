class R1(torch.nn.Module):
    """
    Implementation of R1 GAN regularization
    """

    def __init__(self):
        super(R1, self).__init__()

    def forward(self, prediction_real: torch.Tensor, real_sample: torch.Tensor) -> torch.Tensor:
        """
        Forward pass to compute the regularization
        :param prediction_real: (torch.Tensor) Prediction of the discriminator for a batch of real images
        :param real_sample: (torch.Tensor) Batch of the corresponding real images
        :return: (torch.Tensor) Loss value
        """
        # Calc gradient
        grad_real = torch.autograd.grad(outputs=prediction_real.sum(), inputs=real_sample, create_graph=True)[0]
        # Calc regularization
        regularization_loss: torch.Tensor = grad_real.pow(2).view(grad_real.shape[0], -1).sum(1).mean()
        return regularization_loss


def predict_x_from_eps(z, eps, alpha_prod):
    """x = (z - sigma*eps)/alpha."""
    while len(alpha_prod.shape) < len(eps.shape):
        alpha_prod = alpha_prod.unsqueeze(-1)
    return (z - eps * torch.sqrt(1 - alpha_prod)) * torch.rsqrt(alpha_prod)


def predict_x_from_v(z, v, alpha_prod):
    """x = (alpha * z - sigma * v)"""
    while len(alpha_prod.shape) < len(v.shape):
        alpha_prod = alpha_prod.unsqueeze(-1)
    return torch.sqrt(alpha_prod) * z - torch.sqrt(1 - alpha_prod) * v


def predict_eps_from_x(z, x, alpha_prod):
    while len(alpha_prod.shape) < len(x.shape):
        alpha_prod = alpha_prod.unsqueeze(-1)
    return (z - x * torch.sqrt(alpha_prod)) * torch.rsqrt(1 - alpha_prod)


